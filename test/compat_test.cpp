template <typename T>
void walk_tree(reader::filesystem_v2 const& fs, T& cb,
               std::optional<reader::dir_entry_view> dev = std::nullopt) {
  if (!dev) {
    dev.emplace(fs.root());
  }

  cb(*dev);

  auto iv = dev->inode();

  if (iv.is_directory()) {
    auto dir = fs.opendir(iv);
    assert(dir);

    for (auto e : *dir) {
      walk_tree(fs, cb, e);
    }
  }
}

int compare_versions(std::string_view v1str, std::string_view v2str) {
  auto const v1 = split_to<std::vector<int>>(v1str, '.');
  auto const v2 = split_to<std::vector<int>>(v2str, '.');

  for (size_t i = 0; i < std::max(v1.size(), v2.size()); ++i) {
    auto const n1 = i < v1.size() ? v1[i] : 0;
    auto const n2 = i < v2.size() ? v2[i] : 0;

    if (n1 > n2) {
      return 1;
    }

    if (n1 < n2) {
      return -1;
    }
  }

  return 0;
}

