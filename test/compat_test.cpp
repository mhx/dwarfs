template <class F>
void walk_json(nlohmann::json& j, F f) {
  for (auto& [k, v] : j.items()) {
    f(v);

    if (v.is_structured()) {
      walk_json(v, f);
    }
  }
}

void remove_inode_numbers(nlohmann::json& j) {
  walk_json(j, [](nlohmann::json& j) {
    if (j.contains("inode")) {
      j.erase("inode");
    }
  });
}

